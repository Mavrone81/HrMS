async function main() {
  console.log('Seeding Employees...');

  for (const e of employees) {
    const encryptedFields = {
      nricEncrypted: encrypt(e.nric),
      basicSalaryEncrypted: encrypt(e.basicSalary),
      bankAccountEncrypted: encrypt(e.bankAccount),
      homeAddressEncrypted: encrypt(e.homeAddress),
    };

    await prisma.employee.upsert({
      where: { employeeCode: e.employeeCode },
      update: encryptedFields,
      create: {
        id: uuidv4(),
        employeeCode: e.employeeCode,
        fullName: e.fullName,
        preferredName: e.preferredName,
        nricEncrypted: encrypt(e.nric),
        nricType: e.nricType,
        citizenshipStatus: e.citizenshipStatus,
        nationality: e.nationality,
        dateOfBirth: e.dateOfBirth,
        gender: e.gender,
        race: e.race,
        maritalStatus: e.maritalStatus,
        workEmail: e.workEmail,
        department: e.department,
        designation: e.designation,
        employmentType: e.employmentType,
        startDate: e.startDate,
        isActive: true,
        basicSalaryEncrypted: encrypt(e.basicSalary),
        bankName: e.bankName,
        bankAccountEncrypted: encrypt(e.bankAccount),
        homeAddressEncrypted: encrypt(e.homeAddress),
        passType: e.passType || null,
        passNumber: e.passNumber || null,
        passExpiryDate: e.passExpiryDate || null,
        cpfPrYear: e.cpfPrYear || null,
        noticePeriodDays: e.noticePeriodDays || 30,
      },
    });
    console.log(`  ✓ ${e.employeeCode} — ${e.fullName}`);
  }

  console.log(`\nEmployee seeding complete. ${employees.length} records upserted.`);
}

main()
  .catch(e => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
